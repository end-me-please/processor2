function helpCmd(handler){
    let embedString="";
    let title="all commands";
    let commands=Object.values(command.list);
    if(handler.flags.category){
        commands=commands.filter(c=>c.category==handler.flags.category);
        title="category: "+handler.flags.category;
    }

    if(commands.length==0){handler.textReply("no commands found!");return;}

    //check if first argument is a command name
    if(handler.args[0] && commands.find(c=>c.name==handler.args[0])){
        let command=commands.find(c=>c.name==handler.args[0]);
        embedString+="description: ";
        embedString+=command.description+"\n";
        embedString+="usage: "+config.prefix+" "+command.name+" "+command.args+"\n";
        embedString+="category: "+command.category+"\n";
        embedString+="admin: "+command.admin+"\n";
        embedString+="nsfw: "+command.nsfw+"\n";
        if(Object.keys(command.flags).length>0){
            //read each entry in flags and add it to the embed, as name and value
            embedString+="flags: ```[";
            Object.entries(command.flags).forEach(([key,value])=>{
                embedString+=key+"; "+value+", ";
            });
            embedString = embedString.slice(0, -2);
            embedString+="]```";
        }
        title=command.name;
    } else {
    //list all commands
    commands.forEach(c=>{
        embedString+="**"+c.name+"**:";
        embedString+=c.description+"\n";
    });
    }
    
    if(handler.flags.raw){
        handler.textReply(embedString);
        return;
    }
    handler.listEmbedReply(title,"help",embedString);

}
let help=new command("help",helpCmd,["word"],"Get help about a command","general");


